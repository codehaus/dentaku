package org.omg.uml.diagraminterchange;

/**
 * Dimension structure interface.
 */
public interface Dimension extends javax.jmi.reflect.RefStruct {
    /**
     * Returns value of Width field.
     * @return Value of Width field.
     */
    public double getWidth();
    /**
     * Returns value of Height field.
     * @return Value of Height field.
     */
    public double getHeight();
}
