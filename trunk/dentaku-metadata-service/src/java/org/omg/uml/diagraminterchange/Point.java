package org.omg.uml.diagraminterchange;

/**
 * Point structure interface.
 */
public interface Point extends javax.jmi.reflect.RefStruct {
    /**
     * Returns value of X field.
     * @return Value of X field.
     */
    public double getX();
    /**
     * Returns value of Y field.
     * @return Value of Y field.
     */
    public double getY();
}
